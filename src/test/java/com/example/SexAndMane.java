package com.example;

public class SexAndMane {
    String sex;
    boolean mane;

    public SexAndMane(String sex, boolean mane) {
        this.sex = sex;
        this.mane = mane;
    }

    public String getSex() {
        return sex;
    }

    public boolean isMane() {
        return mane;
    }
}
